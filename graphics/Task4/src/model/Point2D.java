package model;

public class Point2D {
    private double u;
    private double v;

    public Point2D(double u, double v) {
        this.u = u;
        this.v = v;
    }

    public double getU() {
        return u;
    }

    public void setU(double u) {
        this.u = u;
    }

    public double getV() {
        return v;
    }

    public void setV(double v) {
        this.v = v;
    }
}
